    private void botonpagarRecargaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonpagarRecargaActionPerformed

        Celular objCelular = new Celular();
        LogicaCelular objLogicaCelular = new LogicaCelular();
        Recargas objRecargas = new Recargas();
        LogicaRecargas objLogicaRecargas = new LogicaRecargas();

        try {
            // 1. Obtener datos de la interfaz
            String numeroCelular = campoNumeroCelular2.getText().trim();
            String valorRecargaStr = campoValorRecarga.getText().trim();

            // 2. Validar campos vacíos
            if (numeroCelular.isEmpty() || valorRecargaStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe completar todos los campos");
                return;
            }

            // 3. Convertir valor a entero
            int valorRecarga = Integer.parseInt(valorRecargaStr);

            // 4. Validar valor positivo
            if (valorRecarga <= 0) {
                JOptionPane.showMessageDialog(this, "El valor debe ser mayor a 0");
                return;
            }

            // 5. Ejecutar lógica de recarga
            LogicaRecargas logica = new LogicaRecargas();
            String resultado = logica.realizarRecarga(numeroCelular, valorRecarga);

            // 6. Mostrar resultado
            JOptionPane.showMessageDialog(this, resultado);

            // 7. Limpiar campos
            campoNumeroCelular2.setText("");
            campoValorRecarga.setText("");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El valor debe ser un número entero");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }


    }//GEN-LAST:event_botonpagarRecargaActionPerformed

