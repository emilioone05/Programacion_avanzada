    private void botonInsertarCelularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInsertarCelularActionPerformed
        Celular objCelular = new Celular();
        LogicaCelular objLogicaCelular = new LogicaCelular();

        // Obtener los datos ingresados
        String numeroCelular = campoNumeroCelular.getText();
        String estadoCelular = campoEstadoCelular.getText();

        // Cargar el objeto Profesor
        objCelular.setNumero(numeroCelular);
        objCelular.setEstado(Integer.valueOf(estadoCelular));

        // REALIZAR INSERCION
        try {
            // Insertar profesor
            objLogicaCelular.Insertar(objCelular);

            // Mostrar mensaje
            JOptionPane.showMessageDialog(this, "CELULAR INGRESADO CON EXITO");

        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "ERROR EN LA INSERCION: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_botonInsertarCelularActionPerformed


