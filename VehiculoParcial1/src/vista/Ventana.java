    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Validar campos vacíos
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()
                || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty()
                || jTextField5.getText().isEmpty() || jTextField6.getText().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // Crear y configurar objeto Propietario
            Propietario propietario = new Propietario();
            propietario.setCedula(jTextField1.getText().trim());
            propietario.setNombre(jTextField2.getText().trim());
            propietario.setApellido(jTextField3.getText().trim());

            // Crear y configurar objeto Vehiculo
            Vehiculo vehiculo = new Vehiculo();
            vehiculo.setPlaca(jTextField4.getText().trim());
            vehiculo.setMarca(jTextField5.getText().trim());
            vehiculo.setEstado(jTextField6.getText().trim());

            // Insertar en la base de datos
            LogicaPropietario logica = new LogicaPropietario();
            logica.registrarPropietario(propietario);
            logica.registrarVehiculo(vehiculo);

            // Mostrar mensaje de éxito
            JOptionPane.showMessageDialog(this, "Registro exitoso",
                    "Éxito", JOptionPane.INFORMATION_MESSAGE);

            // Limpiar campos después del registro
            limpiarCampos();

        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Error: Driver JDBC no encontrado",
                    "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error de base de datos: " + ex.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed
    private void limpiarCampos() {
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField1.requestFocus(); 
    }
